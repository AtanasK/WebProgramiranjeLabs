export class Student {
  public id: string;
  public name: string;
  public surname: string;
  public index: string;
  public nasoka: string;

  constructor(id: string, name: string, surname: string, index: string, nasoka: string) {
    this.id = id;
    this.name = name;
    this.surname = surname;
    this.index = index;
    this.nasoka = nasoka;
  }
}
